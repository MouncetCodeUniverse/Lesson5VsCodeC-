// comparison and logical operators
// عمليات المقارنة والعمليات المنطقية

// comparison operators عمليات المقارنة
System.Console.WriteLine(3 < 6);
System.Console.WriteLine(3 > 6);
System.Console.WriteLine(   6<=6);
System.Console.WriteLine(   6>=6);
System.Console.WriteLine(   7 == 7);
System.Console.WriteLine(   5 != 4);

// Logical operators العمليات المنطقية
System.Console.WriteLine(3 < 6 && 4 < 7);
System.Console.WriteLine(3 < 6 && 4 > 7);
System.Console.WriteLine(3 < 6 || 4 > 7);
System.Console.WriteLine(!(2 == 2));





